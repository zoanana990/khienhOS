#ifndef _TYPE_H_
#define _TYPE_H_

/* unsigned variable type */
typedef unsigned long long u64;
typedef unsigned int       u32;
typedef unsigned short     u16;
typedef unsigned char      u8;

typedef u32 size_t;

/* signed variable */
typedef long long   i64;
typedef int         i32;
typedef short       i16;
typedef char        c8;

typedef i32 ssize_t;

#endif